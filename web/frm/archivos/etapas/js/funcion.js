function validarFormulario(){
    var valor = true;
    if($("#nombre_etapa").val().trim()===""){
        valor = false;
        $("#mensajes").html("El tipo de etapa no puede estar vacio");
        $("#nombre_etapa").focus();
    }
    return valor;
}

function limpiarFormulario() {
    $("#id_etapa").val("0");
    $("#nombre_etapa").val("");
}

