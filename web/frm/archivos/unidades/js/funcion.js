function validarFormulario() {
    var valor = true;
    if ($("#nombre_asignatura").val().trim() === "") {
        valor = false;
        $("#mensajes").html("El nombre de la asignatura no puede estar vacio");
        $("#nombre_asignatura").focus();
    } else {
        if ($("#descripcion_unidad").val().trim() === "") {
            valor = false;
            $("#mensajes").html("La descripcion de la  no puede estar vacia");
            $("#descripcion_unidad").focus();
        }
    }
    return valor;
}

function limpiarFormulario() {
    $("#id_unidad").val("0");
    $("#id_asigantura").val("0");
    $("#nombre_asigantura").val("");
    $("#descripcion_unidad").val("");
}